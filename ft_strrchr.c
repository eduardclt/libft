#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;
	char	*s1;

	s1 = (char *)str;
	i = 0;
	while (*s1 != '\0')
	{
		i++;
		s1++;
	}
	while (i >= 0)
	{
		if (*s1 == c)
			return (s1);
		s1--;
		i--;
	}
	return (NULL);
}
